function volKubus(sisi) {
  return 'Volume kubus dengan sisi ' + sisi + 'cm' + ' adalah: ' + (sisi*sisi*sisi) + 'cm^3'
}

function volDuaKubus(a, b) {
  return 'Volume dari kubus a dan kubus b: ' + ((a*a*a) + (b*b*b) + 'cm^3')
}

