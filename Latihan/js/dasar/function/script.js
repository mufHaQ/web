// Function

function tambah(a, b) {
  return 'Hasil dari ' + a + ' + ' + b + ' adalah: ' + (a + b)
}

function kali(a, b) {
  return 'Hasil dari ' + a + ' x ' + b + ' adalah: ' + (a * b)
}

